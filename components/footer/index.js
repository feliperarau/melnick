/**
 * Footer Component
 *
 * @returns void
 */
const footer = () => {
    const ref = document.querySelectorAll("._footer");

    if (!ref.length) return;

    // For every component on the page...
    for (let c = 0; c < ref.length; c++) {
        const component = ref[c];
    }
};

footer();

export default footer;
