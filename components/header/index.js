/**
 * Header Component
 *
 * @returns void
 */
const header = () => {
  const ref = document.querySelectorAll("._header");

  if (!ref.length) return;

  // For every component on the page...
  for (let c = 0; c < ref.length; c++) {
    const component = ref[c];
  }
};

header();

export default header;
